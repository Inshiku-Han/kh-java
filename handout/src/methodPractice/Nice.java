package methodPractice;

public class Nice {
	private String a;
	private String b;
	public String getA() {
		return a;
	}
	public void setA(String a) {
		this.a = a;
	}
	public String getB() {
		return b;
	}
	public void setB(String b) {
		this.b = b;
	}
	public void display() {
		System.out.println(getA() + " " + getB());
	}
	
	
}

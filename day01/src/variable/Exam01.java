package variable;

public class Exam01 {
	public static void main(String[] args) {
		//출력 후 한줄 기행
		System.out.println("안녕하세요"); // 안녕하세요
		System.out.println("aa" + "bb"); // aabb
		System.out.println("aa" + 10 + "bb"); // aa10bb
		System.out.println(20 + 10 + "bb"); // 30bb
		System.out.println("" + 10 + 20); // 1020
		System.out.println(10 + 20 + "" + 10); // 3010
	}
}

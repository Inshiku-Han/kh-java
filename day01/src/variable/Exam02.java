package variable;

public class Exam02 {
	public static void main(String[] args) {
		//변수의 선언
		//자료형 변수명;
		int num1;
		num1 = 10;
		
		int num2 = 20;
		int num3 = num1 + num2;
		System.out.println("num1 = " + num1); // 10
		System.out.println("num2 = " + num2); // 20
		System.out.println("num3 = " + num3); // 30
	}
}
